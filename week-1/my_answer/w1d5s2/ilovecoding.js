//1. Melakukan Looping Menggunakan For

console.log('LOOPING FOR PERTAMA')

var i;
for (i=1;i<=20;i++) {
    console.log(i + ' - I love coding');
}

console.log('LOOPING FOR KEDUA')
var j;
for (j=20; j>=1; j--) {
    console.log(j + ' - I will become fullstack developer');
}

//2. While
console.log('LOOPING FOR PERTAMA')

var i=2;
while(i <=20) {
    console.log(i + ' - I love coding');
    i+=2;
}

console.log('LOOPING FOR KEDUA')

var j=20;
while(j >=2) {
    console.log(j + ' - I will become fullstack developer');
    j-=2;
}